class User:
    def __init__(self):
        self.id = ''
        self.name = ''
        self.age = 0
        self.sex = ''
        self.city = ''
        self.photo = ''

    def set_id(self, id):
        self.id = id

    def set_name(self, name):
        self.name = name

    def set_age(self, age):
        self.age = age

    def set_sex(self, sex):
        self.sex = sex

    def set_city(self, city):
        self.city = city

    def set_photo(self, photo):
        self.photo = photo
